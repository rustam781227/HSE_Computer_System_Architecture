#ifndef ACS_HW1_RND_H
#define ACS_HW1_RND_H

#include <cstdlib>

//------------------------------------------------------------------------------
// rnd.h - содержит генератор случайных чисел в диапазоне от 1 до 20
//------------------------------------------------------------------------------

inline auto Random(int a = 1, int b = 20) {
    return a + (rand() % (abs((a - b)) + 1));
}

#endif //ACS_HW1_RND_H
